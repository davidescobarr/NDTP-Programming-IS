export type StaticTestResult = {
    text: string;
    scores?: Record<string, number>;
    totalScore?: number;
};

const getAnsweredId = (question: StaticQuestion) => question.answer;

const formatScores = (scores: Record<string, number>) =>
    Object.entries(scores)
        .map(([category, score]) => `${category}: ${score}`)
        .join('\n');

const createCategoryAnalyzer = (
    questions: StaticQuestion[],
    categoryDefinitions: Record<string, string[]>,
    suffixes: string[],
    resultTexts: Record<string, string>,
): StaticTestResult => {
    const scores = Object.keys(categoryDefinitions).reduce<Record<string, number>>((acc, category) => {
        acc[category] = 0;
        return acc;
    }, {});

    questions.forEach((question, index) => {
        const answerId = getAnsweredId(question);
        const suffix = typeof answerId === 'number' ? suffixes[answerId] : undefined;
        if (!suffix) return;

        const effectiveId = `${index + 1}${suffix}`;
        Object.entries(categoryDefinitions).forEach(([category, ids]) => {
            if (ids.includes(effectiveId)) scores[category] += 1;
        });
    });

    const winningCategory = Object.keys(scores).reduce((bestCategory, category) =>
        scores[category] > scores[bestCategory] ? category : bestCategory,
    Object.keys(scores)[0]);

    return {
        text: `${resultTexts[winningCategory] ?? 'Спасибо за прохождение теста! Ваши ответы учтены.'}\n\nБаллы по категориям:\n${formatScores(scores)}`,
        scores,
    };
};

